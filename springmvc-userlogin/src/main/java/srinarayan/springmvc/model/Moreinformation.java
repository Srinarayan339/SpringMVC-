package srinarayan.springmvc.model;

public class Moreinformation { // Getter Setter of field of Moreinformation

	private String firstname;
	private String lastname;
	private String address;
	private long mobile;
	private String qualification;
	private String engprof;

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getLastname() {
		return lastname;
	}

	public void setLastname(String lastname) {
		this.lastname = lastname;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public long getMobile() {
		return mobile;
	}

	public void setMobile(long mobile) {
		this.mobile = mobile;
	}

	public String getQualification() {
		return qualification;
	}

	public void setQualification(String qualification) {
		this.qualification = qualification;
	}

	public String getEngprof() {
		return engprof;
	}

	public void setEngprof(String engprof) {
		this.engprof = engprof;
	}
}
